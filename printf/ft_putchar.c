#include "libft.h"

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

void	ft_putchar(char c, struct s_stock_str *par)
{
	write(1, &c, 1);
	par->r = par->r + 1;
}
